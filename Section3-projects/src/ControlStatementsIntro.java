public class ControlStatementsIntro {

    public static void main(String[] args)
    {
        int age=16;

        if(age>=15)
            System.out.println("You can drive");
        else
            System.out.println("You cannot drive");

        for(int i=0;i<age;i++)
            System.out.println("Happy Birthday " +i);
    }
}
