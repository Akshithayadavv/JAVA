public class RepetitionFun {
    public static void main(String[] args)
    {
        int count=0;

        while(count <10)
        {
            System.out.println(count);
            count++;
        }
        System.out.println();
        int count1=15;
        do{
            System.out.println(count1);
            count1++;
        }
        while(count1<10);
    }
}
